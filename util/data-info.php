  <li id="setting">
    <h3>Setting</h3>
    This provides information about the environment a school is located in. Possible values are:
    <ul class="bullet">
      <li>Large City (Inside a city with 250,000 residents or more)</li>
      <li>Midsize City (Inside a city with between 100,000 and 250,000 residents)</li>
      <li>Small City (Inside a city with less than 100,000 residents)</li>
      <li>Large Suburb (Outside a city, but in an area with 250,000 or more residents)</li>
      <li>Midsize Suburb (Outside a city and in an area with between 100,000 and 250,000 residents)</li>
      <li>Small Suburb (Outside a city and in an area with less than 100,000 residents)</li>
      <li>Fringe Town (In an area that is less than 10 miles from an urbanized area)</li>
      <li>Distant Town (In an area that is between 10 and 35 miles from an urbanized area)</li>
      <li>Remote Town (In an area that is more than 35 miles from an urbanized area)</li>
      <li>Fringe Rural (In a rural area that is less than 5 miles from an urbanized area or less than 2.5 miles from an urban cluster)</li>
      <li>Distant Rural (In a rural area that is between 5 and 25 miles from an urbanized area or between 2.5 and 10 miles from an urban cluster)</li>
      <li>Remote Rural (In a rural area than is more than 25 miles from an urbanized area and more than 10 miles from an urban cluster)</li>
    </ul>
    Note that the American Samoa, the Commonwealth of the Northern Mariana Islands, Guam, and the Virgin Islands are not structured to fit into these categories and therefore appear as "Unknown".
  </li>

  <li id="score-ranges">
    <h3>SAT/ACT Ranges</h3>
    Standardized testing score ranges indicate the middle 50% of all accepted students. In other words, half of the students accepted to a school will fit into the displayed range, specifically, the half closest to the median. SAT composite scores can lie between 600 and 2400, and SAT subscores can lie between 200 and 800. ACT scores can lie between 1 and 36.
  </li>

  <li id="disabled">
    <h3>Disabled</h3>
    This is the percentage of enrolled undergraduates who are formally registered as students with disabilities. This number is only displayed when it is greater than 3%. Note that when this data point is unknown, the value is not necessarily less than 3%.
  </li>

  <li id="level">
    <h3>Level</h3>
    This provides information about the length of the school's programs. Possible values are:
    <ul class="bullet">
      <li>4+ Years</li>
      <li>At least 2 but less than 4 years</li>
      <li>Less than 2 years</li>
    </ul>
  </li>

  <li id="tribal">
    <h3>Tribal Colleges and Universities</h3>
    This indicates whether or not a school is a Tribal College or University. Most of these schools are tribally controlled and are located on reservations. All of them are members of the American Indian Higher Education Consortium.
  </li>

  <li id="land-grant">
    <h3>Land Grant Universities</h3>
    <p>From the National Center for Education Statistics:</p>
    <p>A land-grant college or university is an institution that has been designated by its state legislature or Congress to receive the benefits of the Morrill Acts of 1862 and 1890. The original mission of these institutions, as set forth in the first Morrill Act, was to teach agriculture, military tactics, and the mechanic arts as well as classical studies so that members of the working classes could obtain a liberal, practical education.</p>
    Over the years, land-grant status has implied several types of federal support. The first Morrill Act provided grants in the form of federal lands to each state for the establishment of a public institution to fulfill the act's provisions. At different times money was appropriated through legislation such as the second Morrill Act and the Bankhead-Jones Act, although the funding provisions of these acts are no longer in effect. Today, the Nelson Amendment to the Morrill Act provides a permanent annual appropriation of $50,000 per state and territory.</p>
    A key component of the land-grant system is the agricultural experiment station program created by the Hatch Act of 1887. The Hatch Act authorized direct payment of federal grant funds to each state to establish an agricultural experiment station in connection with the land-grant institution there. The amount of this appropriation varies from year to year and is determined for each state through a formula based on the number of small farmers there. A major portion of the federal funds must be matched by the state.</p>
    To disseminate information gleaned from the experiment stations' research, the Smith-Lever Act of 1914 created a Cooperative Extension Service associated with each U.S. land-grant institution. This act authorized ongoing federal support for extension services, using a formula similar to the Hatch Act's to determine the amount of the appropriation. This act also requires that the states provide matching funds in order to receive the federal monies.</p>
    Passage of the First Morrill Act (1862) reflected a growing demand for agricultural and technical education in the United States. While a number of institutions had begun to expand upon the traditional classical curriculum, higher education was still widely unavailable to many agricultural and industrial workers. The Morrill Act was intended to provide a broad segment of the population with a practical education that had direct relevance to their daily lives.</p>
    The Second Morrill Act (1890) sought to extend access to higher education by providing additional endowments for all land-grants, but prohibiting distribution of money to states that made distinctions of race in admissions. However, states that provided a separate land-grant institution for blacks were eligible to receive the funds. The institutions that, as a result of this act, were founded or designated the land-grant for blacks in each of the then-segregated Southern states came to be known as "the 1890 land-grants." Native American tribal colleges are sometimes called the "1994 land-grants."</p>
  </li>

  <li id="distance">
    <h3>Distance Programs</h3>
    Distance education is education that uses various technologies to instruct students who are separated from the instructor.
  </li>

